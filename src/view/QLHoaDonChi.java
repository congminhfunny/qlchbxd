    private void fillDataToTable() {
        try {
            listHoaDonChi = new ArrayList<>();
            listHoaDonChi = HoaDonChiDAO.getHDC();
            if (listHoaDonChi == null) {
                return;
            }
            model.setRowCount(0);
            for (HoaDonChi o : listHoaDonChi) {
                model.addRow(new Object[]{o.getMaHoaDonChi(), o.getMucDich(), o.getThanhTien(), o.getNgayTao(), o.getMaNhanVien()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void fillDataToCombobox() {
        try {
            listNhanVien = new ArrayList<>();
            listNhanVien = HoaDonChiDAO.getNhanVien();
            cboMaNhanVien.removeAllItems();
            for (NhanVien o : listNhanVien) {
                cboMaNhanVien.addItem(o.getMaNhanVien() + "");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void setTenNV() {
        try {
            String tenNV = "Không có tên";
            int manv = -1;
            for (NhanVien o : listNhanVien) {
                manv = o.getMaNhanVien();
            }
            if (manv == -1 || cboMaNhanVien.getItemCount() == 0) {
                return;
            }
            for (NhanVien o : listNhanVien) {
                if (o.getMaNhanVien() == Integer.parseInt(cboMaNhanVien.getSelectedItem().toString())) {
                    tenNV = o.getHoTen();
                    txtTenNV.setText(tenNV);
                    return;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private boolean checkNull() {
        if (txtMucDich.getText().isEmpty() || txtNgayChi.getText().isEmpty() || txtSoTien.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Các mục không được để trống");
            return true;
        } else {
            return false;
        }
    }

    private boolean checkSo() {
        try {
            int so = Integer.parseInt(txtSoTien.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Số tiền phải là số nguyên");
            return true;
        }
        return false;
    }

    private boolean checkNC() {
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Date date = sdf.parse(txtNgayChi.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Định dạng ngày phải là : yyyy-MM-dd");
            return true;
        }
        return false;
    }

