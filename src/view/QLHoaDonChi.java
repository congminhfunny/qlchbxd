    private void btnTaoHoaDonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoHoaDonActionPerformed
        // TODO add your handling code here:
        try {
            if (checkNull()) {
                return;
            }
            if (checkSo()) {
                return;
            }
            if (checkNC()) {
                return;
            }
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn tạo hóa đơn này?");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            int soTien = Integer.parseInt(txtSoTien.getText());
            HoaDonChiDAO.saveHDC(txtMucDich.getText(), soTien, txtNgayChi.getText(), Integer.parseInt(cboMaNhanVien.getSelectedItem().toString()));
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
            fillDataToTable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnTaoHoaDonActionPerformed

