    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            if (index == -1) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn hóa đơn muốn sửa trong bảng");
                return;
            }
            if (checkNull()) {
                return;
            }
            if (checkSo()) {
                return;
            }
            if (checkNC()) {
                return;
            }
            int hoi = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn sửa hóa đơn này?");
            if (hoi != JOptionPane.YES_OPTION) {
                return;
            }
            int soTien = Integer.parseInt(txtSoTien.getText());
            HoaDonChiDAO.updateHDC(txtMucDich.getText(), soTien, txtNgayChi.getText(), Integer.parseInt(cboMaNhanVien.getSelectedItem().toString()), Integer.parseInt(tblChiTieu.getValueAt(index, 0).toString()));
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
            fillDataToTable();
            index = -1;
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSuaActionPerformed


