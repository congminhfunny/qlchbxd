    private void loadDataToComboboxMSP() {
        try {
            cboMaSanPham.removeAllItems();
            listSanPham = BanHangDAO.getSanPham();
            int maLoaiSanPham = -1;
            int maLoaiThuongHieu = -1;
            for (LoaiSanPham o : listLoaiSanPham) {
                if (cboLoaiSanPham.getSelectedItem().toString().equalsIgnoreCase(o.getTenLoaiSanPham())) {
                    maLoaiSanPham = o.getMaLoaiSanPham();
                    break;
                }
            }
            for (ThuongHieu o : listThuongHieu) {
                if (cboThuongHieu.getSelectedItem().toString().equalsIgnoreCase(o.getTenThuongHieu())) {
                    maLoaiThuongHieu = o.getMaThuongHieu();
                    break;
                }
            }
            for (SanPham o : listSanPham) {
                if (o.getMaLoaiSanPham() == maLoaiSanPham && o.getMaLoaiThuongHieu() == maLoaiThuongHieu) {
                    cboMaSanPham.addItem(o.getMaSanPham() + "");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

