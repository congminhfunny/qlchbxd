    private void fillToTable() {
        try {
            int msp = Integer.parseInt(cboMaSanPham.getSelectedItem().toString());
            String tsp = txtTenSanPham.getText();
            int sl = Integer.parseInt(txtSoLuong.getText());
            int gt = Integer.parseInt(txtGiaThanh.getText());
            int tt = (gt * sl);
            if (tblHoaDon.getRowCount() == 0) {
                model.addRow(new Object[]{msp, tsp, sl, tt});
                return;
            }
            if (0 < tblHoaDon.getRowCount()) {
                for (int i = 0; i < tblHoaDon.getRowCount(); i++) {
                    if (msp == Integer.parseInt(tblHoaDon.getValueAt(i, 0).toString())) {
                        model.addRow(new Object[]{msp, tsp, sl + Integer.parseInt(tblHoaDon.getValueAt(i, 2).toString()), tt + Integer.parseInt(tblHoaDon.getValueAt(i, 3).toString())});
                        model.removeRow(i);
                        return;
                    }
                }
            }
            for (int i = 0; i < tblHoaDon.getRowCount(); i++) {
                if (msp != Integer.parseInt(tblHoaDon.getValueAt(i, 0).toString())) {
                    model.addRow(new Object[]{msp, tsp, sl, tt});
                    return;
                }
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

