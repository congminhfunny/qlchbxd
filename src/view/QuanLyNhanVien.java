            NhanVienDAO.suaNV(txtHoTen.getText(), checkGT(), txtNgaySinh.getText(), txtQueQuan.getText(), txtSoDienThoai.getText(), txtEmail.getText(), tenHinhAnh, checkCV(), maNV);
