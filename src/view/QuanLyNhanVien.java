    private void btnTimKiemNhanVienActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemNhanVienActionPerformed
        // TODO add your handling code here:
        try {
            if (txtMaNhanVienTK.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ô tìm kiếm không được để trống!");
                return;
            }
            if (checkSo()) {
                return;
            }
            for (NhanVien o : listNhanVien) {
                if (o.getMaNhanVien() == Integer.parseInt(txtMaNhanVienTK.getText())) {
                    model.setRowCount(0);
                    model.addRow(new Object[]{o.getMaNhanVien(), o.getHoTen(), checkGT(o.getGioiTinh()), o.getNgaySinh(), o.getQueQuan(), o.getSoDienThoai(), o.getEmail(), o.getAnh(), checkCV(o.getChucVu())});
                    if (o.getAnh() != null) {
                        File file = new File("C:\\Users\\MinhNguyenCong\\Documents\\NetBeansProjects\\QLCHBXD\\src\\imagesnv\\" + o.getAnh());
                        Image img = ImageIO.read(file);
                        int width = 152;
                        int height = 152;
                        lblHinhAnh.setText(null);
                        lblHinhAnh.setIcon(new ImageIcon(img.getScaledInstance(width, height, 0)));
                    } else {
                        File file = new File("C:\\Users\\MinhNguyenCong\\Documents\\NetBeansProjects\\QLCHBXD\\src\\imagesnv\\qlsv1.png");
                        Image img = ImageIO.read(file);
                        int width = 152;
                        int height = 152;
                        lblHinhAnh.setText(null);
                        lblHinhAnh.setIcon(new ImageIcon(img.getScaledInstance(width, height, 0)));
                    }
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "Không tìm thấy!");
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnTimKiemNhanVienActionPerformed

